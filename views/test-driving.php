<h2>About Test-Drive Mode:</h2>

<p class="narrow">
    When test-driving ProPhoto 8, only logged-in admin users will have P8
    as their active theme.  All other users will see your active theme:
    <b><?php echo $nonTestDriveTheme; ?></b>. This allows you to work on your
    conversion to P8 and your updated responsive design in privacy, until you're ready
    to go live.  In the meantime, all of your normal web visitors will see your
    existing design and functionality.
</p>

<p class="narrow">
    If you want to verify what your regular site visitors are seeing, switch to another
    browser you don't normally use, where you are not logged in as an admin for this site.
    Viewing your site that way will show you what your regular visitors are seeing.  Another
    good option is to use "Incognito" or "Private" mode, if your browser supports it.
</p>

<p class="narrow">
    Most people don't log in to their WordPress sites with their phones, so they're not recognized
    by your phone's browser as an admin user.  This means if you want to view your test-driven P8
    site on your phone or other device, you'l need make sure you login to WordPress first.
</p>

